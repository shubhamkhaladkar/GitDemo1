package programs1;

public class Demo2 {
	// unary operator

	public static void main(String[] args) {
		System.out.println("Main method is started..");

		int i = 0;

		int j = 0;

		// pre increment

		++i;

		// post increment

		j++;

		System.out.println("I's value is:" + i);

		System.out.println("J's value is:" + j);
		
		System.out.println("Main method is stopped..");

	}

}
